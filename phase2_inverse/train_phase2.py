"""
Training script for Phase 2: Synthetic Inverse Problem.

Goal: Given option prices generated by a known LV surface, recover that
surface using a dual-output PINN.

Pipeline:
    1. Generate ground truth via FDM with the synthetic LV
    2. Sample "observation" points (mimicking market data)
    3. Train dual-output PINN with: data loss + PDE loss + smoothness loss
    4. Validate: compare recovered sigma vs ground truth

Loss:
    L = lam_data * L_data + lam_pde * L_pde + lam_smooth * L_smooth
"""

